    public static List<UserMealWithExcess> filteredByCycles(List<UserMeal> meals, LocalTime startTime, LocalTime endTime, int caloriesPerDay) {
        Map<Integer, Integer> listUserMealOfDay = new HashMap<>();
        for (UserMeal userMeal : meals) {
            listUserMealOfDay.merge(userMeal.getDateTime().getDayOfYear(), userMeal.getCalories(), Integer::sum);
        }

        List<UserMealWithExcess> listUserMealWithExcessFiltered = new ArrayList<>();
        for (UserMeal userMeal : meals) {
            if (TimeUtil.isBetweenHalfOpen(LocalTime.from(userMeal.getDateTime()), startTime, endTime)) {
                boolean excess = listUserMealOfDay.get(userMeal.getDateTime().getDayOfYear()) < caloriesPerDay;
                   listUserMealWithExcessFiltered.add(new UserMealWithExcess(userMeal.getDateTime(),userMeal.getDescription(),userMeal.getCalories(),excess));
            }
        }
        return listUserMealWithExcessFiltered;
    }

